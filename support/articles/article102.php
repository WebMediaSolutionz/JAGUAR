                <h3>How to bulk upload subscriber device information</h3>

                <p>
                    If you have a large number of subscribers or employees, you can save time by uploading subscriber device information in one Excel file. This process applies to both new device purchases and BYOD plans. 
                </p>

                <p>
                    To add new subscribers, you must first activate their service through TELUS IQ. You can do this either by <a href="#">buying new devices with rate plans</a>, or by <a href="#">activating a BYOD (Bring Your Own Device) plan</a>. 
                </p>

                <p>
                    <strong>LOCATION:</strong> Home -> Buy -> Buy Devices OR Activate BYOD Plans
                </p>

                <p>
                    Follow the instructions until you get to the Subscriber Setup page.
                </p>

                <img src="../images/article29_a.png" />

                <p>
                    Click on Advanced Import -> 
                </p>

                <img src="../images/article29_b.png" />

                <p>
                    <strong>Step 1.</strong> Click “Download Excel File”. The Excel File will open on your computer. Save the file where you can easily find it later.
                </p>

                <img src="../images/article29_c.png" />

                <p>
                    <strong>Step 2.</strong> Enter subscriber details into the corresponding fields. Note: <span class="italics">All subscribers within a group must have the same device, rate plan, add-ons and accessories. A maximum of 50 subscribers can be uploaded at one time.</span>
                </p>

                <p>
                    <strong>Instructions:</strong><br />
                    First name - Type first name of subscriber. Cannot exceed 32 characters
                    Last name - Type last name of subscriber. Cannot exceed 30 characters
                    Port - Indicate if you wish to port a number or not (Yes or No)
                    Calling City - To validate the calling city, type the city name into the TELUS IQ subscriber page and use that exact City, Province code in the Calling City column of your Excel file. If you are porting numbers, the Calling City is not required
                    Existing phone number - Required only if you’re porting
                    Account number - Required only if you’re porting
                    Voicemail language - Indicate English or French 
                    SIM - Required when you’re bringing your own SIM (For BYOD Plans, you must bring your own SIM)
                </p>

                <p>Save your Excel file.</p>

                <p>
                    <strong>Step 3.</strong> Upload your file. Click on "Browse files" to find and select your saved Excel file, and click "Upload Excel". 
                </p>

                <p>
                    <strong>To cancel your uploaded file or fix an error</strong>
                </p>

                <p>
                    If you need to cancel your uploaded file, simply hit Cancel, and begin again.
                </p>

                <p>
                    If you see an error message, you won't be able to proceed. There may be blanks in the grid, or there may be problems with the Calling City. 
                </p>

                <p>
                    You can either upload your file again, or you can manually enter the missing information into the blanks in the grid.
                </p>
