                    Before sending the device in for repair, try these basic troubleshooting steps. 
                </p>

                <p>
                    <strong>To submit a device for repair</strong>
                </p>

                <p>
                    On the TELUS IQ home page, you will find the Repairs location in the lower right corner. 
                </p>

                <img src="../images/article28_a.png" />

                <p>
                    You will need to inupt the device’s IMEI in the box provided. 
                </p>

                <p>
                    The IMEI is a 15-digit (no letters) identification number for a mobile device. It can be located in the following ways:
                </p>

                <ul>
                    <li>Look on the side of the device box</li>
                    <li>Look on the back of the phone under the battery</li>
                    <li>Look inside the SIM or SD card tray</li>
                    <li>Remove the battery and locate the small ticker</li>
                    <li>Press *#06# using the keypad on your phone</li>
                </ul>

                <p>
                    If you can’t locate the IMEI, click “Can’t find IMEI”. You will be asked for your contact information and a TELUS representative will be in touch.
                </p>

                <p>
                    Once you enter the device’s IMEI, you are taken to a screen where you are asked to provide details about the device problem. Enter the information required and click “Process Repair”. Try to be as explicit as possible about the problem you’re having with the device and the attempts you made to repair it. All fields are mandatory.
                </p>

                <img src="../images/article28_b.png" />

                <p>
                    TELUS will send you a prepaid waybill via email or mail. Follow the instructions described on the waybill and send the device in for repair.
                </p>

                <p>
                    You’ll be contacted by TELUS when the device is received to confirm any additional repair costs.  (The minimum cost of repair per device is $325.00 CAN)
                </p>

                <p>
                    <strong>Other important things to note:</strong>
                </p>

                <ul>
                    <li>
                        If device is an iPhone, please disable the "Find my iPhone" feature before sending the device in for repair. Additional charges may be applied by Apple if the "Find my iPhone" feature is not disabled. How to disable "Find my iPhone" 
                    </li>
                    <li>
                        Physical or liquid damage is not covered under warranty and a charge for repair is applicable
                    </li>
                </ul>
