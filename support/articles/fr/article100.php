                <h3>Comment puis-je transférer des numéros de téléphone provenant d'un autre fournisseur?</h3>

                <p>
                    Les nouveaux clients de TELUS qui se procurent un nouvel appareil ou qui s'abonnent à un forfait AVPA peuvent transférer le numéro de téléphone qu'ils avaient chez leur ancien fournisseur. 
                </p>

                <ul>
                    <li><span class="italics">Tous les numéros de téléphone mobiles faisant l'objet d'un transfert doivent être actifs.</span></li>
                    <li><span class="italics">Un numéro de téléphone filaire ne peut pas être transféré à un compte de services mobiles.</span></li>
                    <li><span class="italics">Pour effectuer le processus de commande, vous devez avoir une nouvelle carte SIM pour chaque appareil AVPA.</span></li>
                    <li><span class="italics">Tous les appareils associés à des forfaits AVPA doivent être déverrouillés. Consultez la section « Déverrouiller des appareils pour les forfaits AVPA » ci-dessous.</span></li>
                </ul>

                <p>
                    Avant de transférer le numéro de téléphone d'un abonné, vous devez <a href="#">acheter un nouvel appareil et vous abonner à un forfait</a>, ou <a href="#">activer un forfait AVPA.</a>.
                </p>

                <p><strong>CHEMIN:</strong> Accueil -> Acheter -> Acheter des appareils OU Activer un forfait seulement</p>

                <p>Suivez les directives jusqu'à ce que vous atteigniez la page de configuration des abonnés.</p>

                <img src="../../images/article27.png" />

                <p>
                    Entrez les renseignements de chaque abonné associé au même numéro de compte.
                </p>

                <ul>
                    <li>Prénom</li>
                    <li>Nom de famille</li>
                    <li>Indiquez Oui dans la colonne Transfert</li>
                    <li>Numéro de téléphone actuel que vous souhaitez transférer à TELUS</li>
                    <li>Numéro de compte de l'abonné chez l'ancien fournisseur (p. ex. Rogers ou Bell)</li>
                    <li>Langue de la messagerie vocale</li>
                    <li>Numéro SIM (requis pour les forfaits AVPA). Consultez la section <a href="#">Où puis-je trouver le numéro de la carte SIM?</a></li>
                </ul>

                <p>
                    Cliquez sur Enregistrer et continuer.
                </p>

                <p>
                    Pour configurer plusieurs abonnés, vous pouvez faire un chargement par lot. Consultez la section <a href="#">Comment charger par lot les renseignements sur les appareils des abonnés.</a>
                </p>

                <p>
                    <strong>Déverrouiller des appareils pour les forfaits AVPA</strong>
                </p>

                <p>
                    Pour déverrouiller des appareils, vous devez communiquer avec votre ancien fournisseur pour qu'il vous donne un code de déverrouillage pour chaque appareil. Le déverrouillage de certains appareils peut faire l'objet de certaines conditions. Le processus varie d'un fournisseur à l'autre.
                </p>

                <p>
                    <strong>Pour déverrouiller des appareils iPhone:</strong>
                </p>

                <ul class="no_styles">
                    <li>1. Votre ancien fournisseur traitera votre demande de déverrouillage.</li>
                    <li>2. Pour déverrouiller un appareil iPhone, il faut faire une copie de sauvegarde de son contenu, effacer le contenu, puis restaurer le contenu.</li>
                    <li>3. Pour en savoir plus sur le déverrouillage d'un appareil iPhone, consultez la page <a href="https://support.apple.com/en-ca/HT201328">https://support.apple.com/en-ca/HT201328</a></li>
                </ul>

                <p>
                    <strong>Pour déverrouiller des appareils Samsung :</strong>
                </p>

                <ul class="no_styles">
                    <li>1. Votre ancien fournisseur vous donnera un code de déverrouillage.</li>
                    <li>2. Fermez l'appareil.</li>
                    <li>3. Insérez la nouvelle carte SIM.</li>
                    <li>4. Rallumez l'appareil.</li>
                    <li>5. Faites glisser votre doigt sur l'écran et un code de déverrouillage vous sera demandé.</li>
                    <li>6. Entrez le code de déverrouillage.</li>
                </ul>

                <p>
                    <strong>Pour déverrouiller des appareils BlackBerry:</strong>
                </p>

                <ul class="no_styles">
                    <li>1. Votre ancien fournisseur vous donnera un code de déverrouillage.</li>
                    <li>2. À l'écran d'accueil, faites glisser votre doigt à partir du haut de l'écran.</li>
                    <li>3. Appuyez sur Paramètres > Sécurité et confidentialité > Carte SIM.</li>
                    <li>4. Dans la section Verrouillage du réseau téléphonique, appuyez sur un élément verrouillé.</li>
                    <li>5. Entrez le code de déverrouillage lorsqu'on vous le demande.</li>
                </ul>
