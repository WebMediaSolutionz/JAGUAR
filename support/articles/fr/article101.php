                <h3>Que dois-je faire pour envoyer un appareil en réparation?</h3>

                <p>
                    Avant d'envoyer un appareil en réparation, essayez ces quelques étapes de dépannage de base. 
                </p>

                <p>
                    <strong>Pour envoyer un appareil en réparation</strong>
                </p>

                <p>
                    La section Réparations se trouve dans le coin inférieur droit de la page d'accueil du portail Gestion mobile intelligente de TELUS. 
                </p>

                <img src="../../images/article28_a.png" />

                <p>
                    Vous devrez entrer le numéro IMEI dans le champ prévu à cet effet. 
                </p>

                <p>
                    Le numéro IMEI sert à identifier les appareils mobiles. Il est composé de 15 chiffres (aucune lettre). Vous le trouverez de l'une des façons suivantes :
                </p>

                <ul>
                    <li>Regardez sur le côté de l'emballage de l'appareil.</li>
                    <li>Regardez au dos de l'appareil, sous la pile.</li>
                    <li>Regardez dans le tiroir de carte SIM ou de carte SD.</li>
                    <li>Retirez la pile et consultez le petit autocollant.</li>
                    <li>Composez *#06# à l'aide du clavier de l'appareil.</li>
                </ul>

                <p>
                    Si vous n'arrivez pas à trouver le numéro IMEI, cliquez sur «Je ne trouve pas le no IMEI». Vous devrez fournir vos coordonnées et un représentant TELUS communiquera avec vous.
                </p>

                <p>
                    Après avoir entré le numéro IMEI de l'appareil, vous devrez décrire le problème de l'appareil. Entrez les renseignements requis, puis cliquez sur Lancer le processus de réparation. Donnez le plus de détails possible sur le problème et sur vos tentatives pour le régler. Tous les champs sont obligatoires.
                </p>

                <img src="../../images/article28_b.png" />

                <p>
                    TELUS vous enverra un bordereau d'envoi prépayé par courriel ou par la poste. Suivez les instructions qui y sont inscrites, puis envoyez l'appareil en réparation.
                </p>

                <p>
                    TELUS communiquera avec vous lorsqu'elle recevra l'appareil pour vous informer de possibles frais de réparation additionnels, le cas échéant. (Les frais de réparation minimaux sont de 325 $ CA par appareil.)
                </p>

                <p>
                    <strong>Remarques importantes :</strong>
                </p>

                <ul>
                    <li>
                        Si l'appareil est un iPhone, veuillez désactiver la fonction «Localiser mon iPhone» avant de l'envoyer en réparation. Apple exige des frais additionnels si cette fonction n'est pas désactivée. Comment désactiver la fonction Localiser mon iPhone.
                    </li>
                    <li>
                        Les dommages physiques ou liquides ne sont pas couverts par la garantie, et des frais de réparation vont alors s'appliquer.
                    </li>
                </ul>
            </div>
