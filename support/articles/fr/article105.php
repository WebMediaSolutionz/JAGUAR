            <div class="article-content__inner">
                <h3>Où puis-je trouver le numéro de la carte SIM?</h3>

                <p>
                    Le numéro de la carte SIM est constitué de 19 chiffres qui servent à identifier une carte SIM précise. Il est lié à un numéro de téléphone mobile et à un compte en particulier. Le numéro est imprimé au dos de la carte SIM, mais il est aussi stocké dans cette dernière. 
                </p>

                <p>
                    Le numéro de la carte SIM est parfois nommé ICCID (ID de la carte de circuit intégré – Integrated Circuit Card ID)
                </p>

                <p>
                    Il se peut que vous deviez trouver le numéro de la carte SIM pour transférer votre appareil d'un fournisseur à l'autre.
                </p>

                <p>
                    <strong>Pour trouver le numéro de la carte SIM</strong>
                </p>

                <p>
                    S'il est possible d'ouvrir l'appareil et d'en retirer la pile, vous pouvez enlever la carte SIM et trouvez le numéro imprimé au dos de celle-ci. 
                </p>

                <p>
                    Si ce n'est pas possible, vous pouvez trouver le numéro sans devoir retirer la carte de l'appareil.
                </p>

                <p>
                    <strong>Dans le cas d'un iPhone</strong>
                </p>

                <p>
                    Réglages -> Général -> Informations -> ICCID
                </p>

                <p>
                    <strong>Dans le cas d'un appareil Android</strong>
                </p>

                <p>
                    Les systèmes Android varient, mais vous pouvez habituellement trouver le numéro de carte SIM en suivant le chemin ci-dessous :
                </p>

                <p>
                    Paramètres -> À propos ou À propos de l’appareil -> Identité du téléphone ou État -> ICCID ou IMSI
                </p>

                <p>
                    <strong>Si les instructions ci-dessus ne fonctionnent pas</strong>
                </p>

                <p>
                    Vous pouvez télécharger une application simple qui se nomme SIM Card Info. Elle permet d'accéder à des renseignements stockés sur la carte SIM.
                </p>
            </div>
