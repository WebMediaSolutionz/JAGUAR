                <h3>Comment charger par lot les renseignements sur les appareils des abonnés</h3>

                <p>
                    Si vous gérez un grand nombre d'abonnés ou d'employés, vous pouvez gagner du temps en téléchargeant un fichier Excel contenant leurs renseignements. Ce processus est possible tant pour les achats de nouveaux appareils que pour les forfaits AVPA. 
                </p>

                <p>
                    Pour ajouter de nouveaux abonnés, vous devez d'abord activer leur service dans le portail Gestion mobile intelligente de TELUS. Vous pouvez le faire soit en <a href="#">achetant de nouveaux appareils avec des forfaits</a> soit en <a href="#">effectuant la mise en service avec un forfait AVPA</a> (Apportez votre propre appareil). 
                </p>

                <p>
                    <strong>CHEMIN:</strong> Accueil -> Acheter -> Acheter des appareils OU Activer un forfait seulement
                </p>

                <p>
                    Suivez les directives jusqu'à ce que vous atteigniez la page de configuration de l'abonné.
                </p>

                <img src="../../images/article29_a.png" />

                <p>
                    Cliquez sur Import avancé -> 
                </p>

                <img src="../../images/article29_b.png" />

                <p>
                    <strong>Étape 1.</strong> Cliquez sur Télécharger le fichier Excel. Le fichier s'ouvre sur votre ordinateur. Enregistrez-le dans un endroit facilement accessible.
                </p>

                <img src="../../images/article29_c.png" />

                <p>
                    <strong>Étape 2.</strong> Saisissez les renseignements sur les abonnés dans les champs correspondants. Remarque : <span class="italics">L'appareil, le forfait, les options et les accessoires doivent être les mêmes pour tous les abonnés associés à un même groupe. Vous pouvez charger un maximum de 50 abonnés à la fois.</span>
                </p>

                <p>
                    <strong>Directives :</strong><br />
                    Prénom – Saisissez le prénom de l'abonné (maximum de 32 caractères).
                    Nom – Saisissez le nom de famille de l'abonné (maximum de 30 caractères).
                    Transfert – Indiquez si vous désirez transférer un numéro de téléphone ou non (Oui ou Non).
                    Ville d’appel – Pour valider la ville d'appel, saisissez le nom de la ville dans la page des abonnés de Gestion mobile intelligente de TELUS et utilisez la même ville et le même code de province dans la colonne Ville d'appel du fichier Excel. La ville d'appel n'est pas requise dans les cas de transfert de numéro.
                    Numéro de téléphone actuel – Requis seulement si vous faites un transfert. 
                    Numéro de compte – Requis seulement si vous faites un transfert.
                    Langue de la messagerie vocale – Indiquez Français ou Anglais. 
                    SIM – Requis si vous apportez votre propre carte SIM (pour vous abonner à un forfait AVPA, vous devez fournir votre carte SIM).
                </p>

                <p>Enregistrez le fichier Excel.</p>

                <p>
                    <strong>Étape 3.</strong> Téléversez le fichier. Cliquez sur Parcourir les fichiers pour trouver le fichier Excel sauvegardé. Cliquez sur Téléverser le fichier. 
                </p>

                <p>
                    <strong>Pour annuler le téléversement ou corriger une erreur</strong>
                </p>

                <p>
                    Si vous devez annuler le téléversement du fichier, cliquez sur Annuler, puis recommencez.
                </p>

                <p>
                    Un message d'erreur entraînera l'interruption de la procédure. Les causes probables sont des champs manquants ou un problème avec la ville d'appel. 
                </p>

                <p>
                    Vous pouvez téléverser votre fichier à nouveau ou saisir manuellement l'information manquante dans les champs vides.
                </p>
