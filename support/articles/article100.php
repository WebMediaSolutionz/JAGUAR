                <h3>How do I port phone numbers from another carrier?</h3>

                <p>
                    New TELUS customers can port phone numbers from another carrier when purchasing new devices or activating BYOD plans. 
                </p>

                <ul>
                    <li><span class="italics">All mobile phone numbers being ported must be active</span></li>
                    <li><span class="italics">Wireline to wireless numbers cannot be ported</span></li>
                    <li><span class="italics">BYOD devices must have a new SIM card in hand to complete the order process</span></li>
                    <li><span class="italics">All devices for BYOD plans must be unlocked. See "To unlock devices for BYOD plans" below.</span></li>
                </ul>

                <p>
                    Before porting subscriber phone numbers, you must <a href="#">buy new devices and rate plans</a>, or <a href="#">activate BYOD plans</a>.
                </p>

                <p><strong>LOCATION:</strong> Home -> Buy -> Buy Devices OR Activate BYOD Plans</p>

                <p>Follow the instructions until you get to the Subscriber Setup page.</p>

                <img src="../images/article27.png" />

                <p>
                    Add information for each subscriber linked to the same Billing Account Number:
                </p>

                <ul>
                    <li>First name</li>
                    <li>Last name</li>
                    <li>Indicate <strong>Yes</strong> in the Port column</li>
                    <li>The existing phone number that you want to port</li>
                    <li>The account number of the old subscriber (i.e. Rogers or Bell)</li>
                    <li>Voicemail language</li>
                    <li>SIM number (for BYOD Plans) (Read <a href="#">Where can I find the SIM number?</a>)</li>
                </ul>

                <p>
                    Save and Continue
                </p>

                <p>
                    If you have a number of subscribers, you can do a bulk upload. See "<a href="#">How to bulk upload subscriber device  information.</a>"
                </p>

                <p>
                    <strong>To unlock devices for BYOD Plans</strong>
                </p>

                <p>
                    To unlock devices, you must contact your previous carrier to recieve an unlock code for each device. There may be certain requirements to qualify for a device to be unlocked. The process for unlocking devices varies by carrier.
                </p>

                <p>
                    <strong>To unlock iPhone devices:</strong>
                </p>

                <ul class="no_styles">
                    <li>1. Your previous carrier will process your unlock request</li>
                    <li>2. You'll back up, erase, and restore each iPhone to unlock it</li>
                    <li>3. Read more information on unlocking iPhone devices here <a href="https://support.apple.com/en-ca/HT201328">https://support.apple.com/en-ca/HT201328</a></li>
                </ul>

                <p>
                    <strong>To unlock Samsung devices:</strong>
                </p>

                <ul class="no_styles">
                    <li>1. Your previous carrier will provide an unlock code</li>
                    <li>2. Turn off the Samsung phone</li>
                    <li>3. Insert a new SIM card</li>
                    <li>4. Turn on the phone</li>
                    <li>5. Slide to unlock and it will ask for the unlock code</li>
                    <li>6. Enter the unlock code</li>
                </ul>

                <p>
                    <strong>To unlock BlackBerry devices:</strong>
                </p>

                <ul class="no_styles">
                    <li>1. Your previous carrier will provide an unlock code</li>
                    <li>2. On the home screen, swipe down from the top of the screen</li>
                    <li>3. Tap Settings > Security and Privacy > SIM Card</li>
                    <li>4. In the Phone Network Lock section, tap a locked item</li>
                    <li>5. When prompted, enter the unlock code</li>
                </ul>
