                                <strong>What do the actions in the Actions drop‐down menu mean?</strong>
                            </p>

                            <img src="../images/article8.jpg">

                            <p>
                                The actions in the drop‐down menu under Actions allow you to make changes to your subscribers’ plans. Before you can make any changes to subscribers’ plans, you must first select the subscriber(s) whose plans will be affected.
                            </p>

                            <p>A table format would be easier to read.</p>

                            <p>
                                Change Rate Plan – this action allows you to change a subscriber’s rate plan. Note that you can only change rate plans within the category negotiated in your company contract. See How do I change rate plans?
                            </p>

                            <p>
                                Change Status – Use this feature to suspend a rate plan if a subscriber’s device is lost or stolen, and to reactivate a rate plan if a lost phone is found. See An employee’s device is lost or stolen. How do I suspend the rate plan?
                            </p>

                            <p>
                                Add Features – You can add features to subscribers’ rate plans by selecting Add Features, but all selected subscribers must have the same rate plan.
                            </p>

                            <p>
                                Remove Features ‐ You can remove features from subscribers’ rate plans by selecting Remove Features, but all selected subscribers must have the same rate plan.
                            </p>

                            <p>
                                Reset VM – Use this feature if a subscriber has forgotten a voice mail password. Selecting Reset VM resets the voice mail to the default password, which is the mobile number of the subscriber. (Payal, how does the subscriber change the password after it has been reset to the default?)
                            </p>

                            <p>
                                Organize – The Organize feature is visible only to the Customer Master Administrator’s account. It is used to tag and group subscribers into company accounts, and can be used to reconfigure subscriber(s) Organization Tags. For example, you would need to reconfigure a subscriber’s account if a company employee moves to a different department in which the subscribers are under a different rate plan. See How to tag subscribers with Organization Tags.
                            </p>
